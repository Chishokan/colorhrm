$note("【権限について】\n・講師（teacher）… 自分のマイページ・打刻・シフト・給与明細を利用します。\n・スタッフ（staff）… 自分の担当教室の講師について、育成・研修承認・シフト・給与を管理します。\n・管理者（admin）… すべての機能（マスター・ユーザー管理を含む）を利用できます。");

// ============================ 2. 共通操作 ============================
$h2('2. 共通操作');
$h3('2.1 ログイン');
$bullet('ブラウザでアプリのURL（例：https://chishokan.co.jp/colorhrm/）を開きます。', '1.');
