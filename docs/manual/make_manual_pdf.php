// ============================ 3. 講師 ============================
$pdf->AddPage();
$h2('3. 講師（teacher）向け');
$h3('3.1 ColorHRM マイページ');
$para('ログイン後の最初の画面です。次のことができます。');
$bullet('自分の現カラー／育成目標カラーと達成率の確認');
