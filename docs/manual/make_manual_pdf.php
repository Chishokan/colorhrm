$bullet('ColorHRM（/colorhrm/）… 採用・育成。研修の進捗・承認、講師情報、カラー制度（WHITE→GREEN→BLUE→YELLOW→RED）。');
$bullet('給与・シフト（/colorhrm-pay/）… 打刻・シフト・給与計算・給与明細。');
