$note('配属教室が未設定だと打刻できません。教室が選べない場合は、管理者・スタッフに配属教室の設定を依頼してください。', [255, 248, 225], [255, 179, 0]);
$h4('シフト可能登録');
$bullet('入れる日だけ開始・終了の時間を入力します（空欄の日は申請しません）。');
$bullet('「↑ 上をコピー」で前の行の時間をコピーできます。');
