$para('スマホでも利用できます。スタッフ画面では左上の三本線（メニューアイコン）をタップするとメニュー（サイドバー）が開きます。講師画面では上部にボタンが並びます。');
$h3('2.4 ログアウト');
$para('共有端末を使う場合は、画面右上の「ログアウト」で必ずログアウトしてください。');
