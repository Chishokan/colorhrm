<?php
// 利用マニュアルPDFを生成（tFPDF + IPAゴシック同梱）。
//   実行: php docs/manual/make_manual_pdf.php  → docs/manual/colorhrm-manual.pdf
require_once __DIR__ . '/../../payroll/tfpdf/tfpdf.php';
require_once __DIR__ . '/../../payroll/tfpdf/font/unifont/ttfonts.php';

$GREEN = [46, 125, 50];
$BLUE  = [21, 101, 192];
$GRAY  = [90, 90, 90];
$DARK  = [34, 34, 34];

$pdf = new tFPDF('P', 'mm', 'A4');
$pdf->SetTitle('Color HRM / 給与・シフト 利用マニュアル');
$pdf->SetAutoPageBreak(true, 18);
$pdf->AddFont('ipag', '', 'ipag.ttf', true);
$pdf->SetMargins(16, 16, 16);
$EPW = 210 - 16 - 16; // 有効幅

// ---- ヘルパ ----
$setColor = function ($c) use ($pdf) { $pdf->SetTextColor($c[0], $c[1], $c[2]); };

$para = function ($txt, $size = 10.5, $color = null, $lh = 5.6) use ($pdf, $setColor, $DARK) {
  $setColor($color ?: $DARK);
  $pdf->SetFont('ipag', '', $size);
  $pdf->MultiCell(0, $lh, $txt);
};

$h2 = function ($txt) use ($pdf, $setColor, $GREEN) {
  if ($pdf->GetY() > 240) { $pdf->AddPage(); }
  $pdf->Ln(4);
  $setColor($GREEN);
  $pdf->SetFont('ipag', '', 15);
  $pdf->MultiCell(0, 8, $txt);
  $y = $pdf->GetY();
  $pdf->SetDrawColor(46, 125, 50);
  $pdf->SetLineWidth(0.5);
  $pdf->Line(16, $y, 210 - 16, $y);
  $pdf->Ln(3);
};

$h3 = function ($txt) use ($pdf, $setColor, $BLUE) {
  if ($pdf->GetY() > 250) { $pdf->AddPage(); }
  $pdf->Ln(2.5);
  $setColor($BLUE);
  $pdf->SetFont('ipag', '', 12.5);
  $pdf->MultiCell(0, 6.5, $txt);
  $pdf->Ln(0.5);
};

$h4 = function ($txt) use ($pdf, $setColor, $DARK) {
  if ($pdf->GetY() > 255) { $pdf->AddPage(); }
  $pdf->Ln(1.5);
  $setColor($DARK);
  $pdf->SetFont('ipag', '', 11);
  $pdf->MultiCell(0, 6, '■ ' . $txt);
};

$bullet = function ($txt, $ordinal = '・') use ($pdf, $setColor, $DARK) {
  $setColor($DARK);
  $pdf->SetFont('ipag', '', 10.5);
  $x = $pdf->GetX();
  $pdf->Cell(6, 5.6, $ordinal, 0, 0);
  $pdf->MultiCell($pdf->GetPageWidth() - $pdf->GetX() - 16, 5.6, $txt);
};

$note = function ($txt, $fill = [240, 247, 240], $border = [46, 125, 50]) use ($pdf, $setColor, $DARK) {
  $pdf->Ln(1.5);
  $setColor($DARK);
  $pdf->SetFont('ipag', '', 10);
  $pdf->SetFillColor($fill[0], $fill[1], $fill[2]);
  $pdf->SetDrawColor($border[0], $border[1], $border[2]);
  $pdf->SetLineWidth(0.3);
  $x = $pdf->GetX(); $y = $pdf->GetY();
  // 枠つきセル
  $pdf->MultiCell(0, 5.4, $txt, 1, 'L', true);
  $pdf->Ln(1.5);
};

// ============================ 表紙 ============================
$pdf->AddPage();
$pdf->Ln(60);
$setColor($GREEN);
$pdf->SetFont('ipag', '', 24);
$pdf->MultiCell(0, 12, "Color HRM ／ 給与・シフト\n利用マニュアル", 0, 'C');
$pdf->Ln(6);
$setColor($GRAY);
$pdf->SetFont('ipag', '', 13);
$pdf->MultiCell(0, 7, "職員・スタッフ／講師　向け", 0, 'C');
$pdf->Ln(40);
$pdf->SetFont('ipag', '', 10);
$pdf->MultiCell(0, 6, "対象システム：ColorHRM（採用・育成）／ 給与・シフト\n※ 画面はログインした権限（講師・スタッフ）に合わせて変わります。", 0, 'C');

